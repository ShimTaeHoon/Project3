package main;

public class Ex3 {

	public static void main(String[] args) {
		
		int result1 = 10 + 2; // 더하기
		int result2 = 10 - 2; // 빼기
		int result3 = 10 * 2; // 곱하기
		int result4 = 10 / 2; // 나누기
		int result5 = 10 % 2; // 나머지
		
		System.out.println(result1); // 12 
		System.out.println(result2); // 8
		System.out.println(result3); // 20
		System.out.println(result4); // 5 몫
		System.out.println(result5); // 0 나머지
		
		// 9 % 3 = 0은 9는 3의 배수
		// 10 % 2 = 0은 나머지가 0이기에 10은 짝수

	}

}
